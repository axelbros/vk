package ru.netology.domain;

public class PostSourse {
    private String type;
    private String platfornm;
    private String date;
    private String url;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPlatfornm() {
        return platfornm;
    }

    public void setPlatfornm(String platfornm) {
        this.platfornm = platfornm;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
